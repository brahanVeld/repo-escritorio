    void cargardatos(){
        if(ide!=-1){
            this._conexion.mariaDB();
           this.descripcion.setText(this._conexion.devolverDato("select Descripcion from clientesInfo where id="+ide+"", "Descripcion")); 
           this._root.setText(this._conexion.devolverDato("select root from clientesInfo where id="+ide+"", "root"));
           this.macropro.setText(this._conexion.devolverDato("select Macropro from clientesInfo where id="+ide+"","Macropro"));
           this.dyndns.setText(this._conexion.devolverDato("select dyndns from clientesInfo where id="+ide+"", "dyndns"));
           this.supervisor.setText(this._conexion.devolverDato("select supervisor from clientesInfo where id="+ide+"", "supervisor"));
           this.admin.setText(this._conexion.devolverDato("select admin from clientesInfo where id="+ide+"", "admin"));
           this.dvr_usuario.setText(this._conexion.devolverDato("select dvr_usuario from clientesInfo where id="+ide+"","dvr_usuario"));
           this.dvr_clave.setText(this._conexion.devolverDato("select dvr_usuario from clientesInfo where id="+ide+"", "dvr_usuario"));
           this.correo.setText(this._conexion.devolverDato("select dvr_correo from clientesInfo where id="+ide+"", "dvr_correo"));
           this.otros_servicios.setText(this._conexion.devolverDato("select otro_servicio from clientesInfo where id="+ide+"","otro_servicio"));
        }
    }
    
    
    
    

