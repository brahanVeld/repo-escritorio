    void guardar(String descripcion, String root, String macropro, String dyndns
    ,String supervisor, String admin, String correo, String dvr_clave, String dvr_usuario, String otros_servicios){
        _conexion.mariaDB();
        
        String sql="";
        if(ide!=-1){
             int validar=JOptionPane.showConfirmDialog(rootPane,"¿Desea actualizar la información del cliente?","Modificar información del cliente", JOptionPane.OK_CANCEL_OPTION);
            
            if(validar==JOptionPane.OK_OPTION){
                sql="update redsoft.clientesInfo set Descripcion='"+descripcion+"',"
                    + "root='"+(root.isEmpty()?"n/a":root)+"',"
                + "Macropro='"+(macropro.isEmpty()?"n/a":macropro)+"',"
                + "dyndns='"+(dyndns.isEmpty()?"n/a":dyndns)+"',"
                + "supervisor='"+(supervisor.isEmpty()?"n/a":supervisor)+"',"
                + "admin='"+(admin.isEmpty()?"n/a":admin)+"',"
                + "dvr_usuario='"+(dvr_usuario.isEmpty()?"n/a":dvr_usuario)+"',"
                + "dvr_clave='"+(dvr_clave.isEmpty()?"n/a":dvr_clave)+"',"
                + "dvr_correo='"+(correo.isEmpty()?"n/a":correo)+"',"
                + "otro_servicio='"+(otros_servicios.isEmpty()?"n/a":otros_servicios)+"'"
                    + "where id="+ide+"";
            }else{
                limpiarinternalFrame(); 
            }
            
        }else{
             sql="insert into redsoft.clientesInfo (Descripcion, root, Macropro, dyndns, supervisor, admin,"
                + "dvr_usuario, dvr_clave, dvr_correo, otro_servicio) values('"+descripcion+"', "
                + "'"+(root.isEmpty()?"n/a":root)+"',"
                + "'"+(macropro.isEmpty()?"n/a":macropro)+"',"
                + "'"+(dyndns.isEmpty()?"n/a":dyndns)+"',"
                + "'"+(supervisor.isEmpty()?"n/a":supervisor)+"',"
                + "'"+(admin.isEmpty()?"n/a":admin)+"',"
                + "'"+(dvr_usuario.isEmpty()?"n/a":dvr_usuario)+"',"
                + "'"+(dvr_clave.isEmpty()?"n/a":dvr_clave)+"',"
                + "'"+(correo.isEmpty()?"n/a":correo)+"',"
                + "'"+(otros_servicios.isEmpty()?"n/a":otros_servicios)+"')";
        
       
        }
        
         _conexion.update(sql);
       
        _conexion.cerrarConeccion();
       this.limpiarinternalFrame();
        
        
        
    }
    
    
